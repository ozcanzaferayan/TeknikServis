    private void islemPDFKaydetBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_islemPDFKaydetBtnActionPerformed

        String fileName = "teknikServisCıktısı.pdf";
        Document document = new Document();
        try {
           PdfWriter writer;
           writer = PdfWriter.getInstance(document, new FileOutputStream(fileName));
           document.open();
           PdfPTable tab =new PdfPTable(islemTbl.getColumnCount());
           tab.setWidthPercentage(100);
            for (int i = 0; i < islemTbl.getColumnCount(); i++) {
                tab.addCell(islemTbl.getColumnName(i));
            }
           
            for(int i=0;i<islemTbl.getRowCount();i++)
            {
                for (int j = 0; j < islemTbl.getColumnCount(); j++) {
                    Object obj = GetData(islemTbl, i, j);
                    String value=obj.toString();
                    tab.addCell(value);
                }
            }
            document.add(tab);
            document.close();
            JOptionPane.showMessageDialog(this, fileName + " adlı dosyaya kaydedildi");
           } catch (Exception e) {
           System.err.println(e.getMessage());
        }
    }//GEN-LAST:event_islemPDFKaydetBtnActionPerformed
    
